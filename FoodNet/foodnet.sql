create database foodnet;

use foodnet;

create table foodclien (
    id_clien int auto_increment not null primary key,
    cpf varchar(14) not null,
    nome varchar(100) not null,
    data_nasc date not null,
    email varchar(50) not null,
    telefone varchar(11) not null,
    data_cad date not null
);

create table enderecos (
    id int auto_increment not null primary key,
    clien_id int not null,
    endereco varchar(100) not null,
    foreign key (clien_id) references foodclien(id_clien) on delete cascade
);
create table foodrestn (
    id_resta int auto_increment not null primary key,
    cnpj varchar(18) not null,
    endereco varchar(100) not null,
    categoria varchar(50) not null,
    hora_abre time not null,
    hora_fecha time not null,
    avalia real not null
);

create table foodprodu (
    id_produ int auto_increment not null primary key,
    nome varchar(100) not null,
    descricao varchar(255) not null,
    categoria varchar(50) not null,
    preco float not null,
    disponi boolean not null,
    resta_id int not null, 
    foreign key (resta_id) references foodrestn(id_resta) on delete cascade
);

create table foodentre(
	id_entre int auto_increment primary key not null,
    nome varchar(100) not null,
    cpf char(11) not null,
    telefone varchar(20) not null,
    placa varchar(50) not null
);

create table foodpedid(
	id_pedid int auto_increment not null primary key,
	data_pedido date not null,
	horario time not null,
	forma_pagam varchar(50) not null,
	status_pedido varchar(50) not null,
    cupom int,
    clien_id int not null,
    resta_id int not null,
    entre_id int not null,
    foreign key (clien_id) references foodclien(id_clien) ,
    foreign key (resta_id) references foodrestn(id_resta) ,
    foreign key (entre_id) references foodentre(id_entre) 
);

create table fooditens(
	id_itens int auto_increment not null primary key,
    produ_id int not null,
    quantidade int not null,
    preco_unit float not null,
    foreign key (produ_id) references foodprodu(id_produ) on delete cascade
);

create table foodavali(
id_avali int auto_increment primary key,
avalia int not null,
comentario varchar(255) not null,
data_avali date,
entre_id int not null,
pedid_id int not null,
resta_id int not null,
foreign key (entre_id) references foodentre(id_entre) on delete cascade,
foreign key (pedid_id) references foodpedid(id_pedid) on delete cascade,
foreign key (resta_id) references foodrestn(id_resta) on delete cascade
);

create table foodcupom(
id_cupom int auto_increment primary key, 
codigo varchar(50),
valor_desconto float,
validade date,
resta_id int not null,
foreign key(resta_id) references foodrestn(id_resta) on delete cascade
);

